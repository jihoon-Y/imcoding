# 06. 형 변환
print(int(3.8)) # 문자열을 정수형으로
print(float(3)) # 문자열을 소수형으로

print(int("2") + int("5")) # 문자열 2와 5를 정수형으로 바꿔서 연산하게 된다
print(float("1.1") + float("2.5"))

print(str(2) + str(5)) # 숫자형을 문자열로

age = 30
print("제 나이는 " + str(age) + "살입니다.")


# 01. 모듈

def add(x, y):
    return x + y


def subtract(x, y):
    return x - y


def multiply(x, y):
    return x * y


def divide(x, y):
    return x / y

# 여러 수학적 계산을 하는 함수들이 있고 이 함수들을 활용해서 프로그램을 만들려고 한다
# 그럼 코드 맨 밑으로 가서 함수들을 사용하면 된다

# 한 파일에 함수도 다 쓰고, 우리가 실제로 실행하려고 하는 코드도 다 쓰다보면
# 하나의 파일이 너무 길어질 수도 있다

# 그래서 코드를 여러 파일로 분리해서 사용하기도 한다
# 01. 숫자형
print(4 + 7) # 덧셈
print(2.0 - 4) # 뺄셈
print(5 * 3) # 곱셈
print(7 % 3) # 7을 3으로 나눈 나머지
print(2 ** 3) # 거듭제곱
# 파이썬에서는 정수형과 정수형 사이에서의 연산 결과는 모두 정수형으로 나온다
# 소수형과 소수형끼리는 소수형이 출력된다
# 소수형이 조금 더 영향력이 있어서 하나라도 소수형이 있으면 모든 결과가 다 그렇게 나온다
print(7 / 2)
print(6 / 2)
print(7.0 / 2)
print(6.0 / 2.0)
# 나눗셈은 항상 소수형으로 출력된다 정수형으로 출력하려면 '형 변환'을 해야한다

print(2 + 2 * 3)
print(2 * (2 + 3)) # 일반적인 사칙연산을 따른다



# 02. 숫자형 심화
# floor division (버림 나눗셈)
print(7 // 2) # 나눗셈과 비슷하지만 소수점은 모두 버린다
print(8 // 3)
print(8.0 // 3) # 버림 나눗셈에서도 소수형을 연산하면 결과도 소수형이 나온다

# round 함수
print(round(3.1415926535, 2)) # 파라미터 값을 정수로 반올림 파라미터가 뭔데!!!! 괄호 안의 값인건가




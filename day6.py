# \r : 커서를 맨 앞으로 이동
print("Red Apple\rPine") #pine을 맨 앞으로 옮기니 같은 byte만큼인 [red ]이 사라짐

# \b : 백스페이스 (한 글자 삭제)
print("Redd\bApple")

# \t : 탭 (4칸 혹은 8칸씩 이동)
print("Red\tApple")